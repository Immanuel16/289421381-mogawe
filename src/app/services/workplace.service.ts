  getWorkplace(query: string, page: number, offset: number, lat: number, lng: number, radius: number, tag: any, tagLogic: string,postalcode: any, city: any) {

    const headers = new HttpHeaders().set("Content-Type", "application/json").set("Token", localStorage.getItem("jwt"));
    let url = AppConstant.PROJECT_SERVICE_ENDPOINT + AppConstant.API_GET_LOCATION + '?';
    url += "q=" + (query == null ? "" : query)
    url += "&page=" + page
    url += "&offset=" + offset
    if (lat) {
      url += "&lat=" + lat
    }
    if (lng) {
      url += "&lng=" + lng
    }
    if (radius != null && radius != 0 ) {
      url += "&radius=" + radius
    }
    var tags = [];
    for(let atag of tag){
      tags.push(encodeURIComponent(atag.value))
    }
    if (tag != null && tag != "") {
      url += "&tag=" + tags
    }
    if (tagLogic != null) {
      url += "&tagLogic=" + tagLogic
    }
    if (postalcode != null && postalcode != "") {
      url += "&postalcode=" + postalcode
    }
    var cities = [];
    for(let citie of city){
      cities.push(encodeURIComponent(citie.value))
    }
    if (city != null && city != "") {
      url += "&city=" + cities
    }
    return this.http.get(url,
      {
        headers
      })
      .pipe(
        map((response) => {
          let res = JSON.parse(JSON.stringify(response));
          if (res.returnValue == '000') {
            return res;
          } else if (res.returnValue == '002') {
            localStorage.removeItem("jwt")
            this.router.navigate(["/signin"])
            throw new Error(res.message);
          } else {
            throw new Error(res.message);
          }
        }),
        catchError((e: Response) => this.handleError(e))
      );
  }

