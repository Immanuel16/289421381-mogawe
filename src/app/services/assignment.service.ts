  getAssignment(query: string, page: number, offset: number, status: string, statusV2: any, isPublished: any, jobName: any, tag: any, tagLogic: string, assignee: any, city: any, filterbatch: any, filterqnair: any, lat: any, lng: any, radius: number) {
    const headers = new HttpHeaders().set("Content-Type", "application/json").set("Token", localStorage.getItem("jwt"));
    let url = AppConstant.PROJECT_SERVICE_ENDPOINT + AppConstant.API_GET_ASSIGNMENT + '?';

    url += "&q=" + (query == null ? "" : query)
    url += "&page=" + page
    url += "&offset=" + offset
    url += "&status=" + status
    if (statusV2 != null && statusV2 != "") {
      url += "&statusV2=" + statusV2
    }
    if (isPublished != null && isPublished != "") {
      url += "&ispublished=" + isPublished
    }
    if (jobName != null && jobName != "") {
      url += "&jn=" + jobName
    }
    var tags = [];
    for (let atag of tag) {
      tags.push(encodeURIComponent(atag.value))
    }
    if (tag != null && tag != "") {
      url += "&tag=" + tags
    }
    if (tagLogic != null) {
      url += "&tagLogic=" + tagLogic
    }
    if (assignee != null && assignee != "") {
      url += "&assignee=" + assignee
    }
    if (filterbatch != null && filterbatch != "") {
      url += "&filterbatch=" + filterbatch
    }
    if (filterqnair != null && filterqnair != "") {
      url += "&filterqnair=" + filterqnair
    }
    if (city != null && city != "") {
      url += "&city=" + city
    }
    if (lat != null && lat != "") {
      url += "&lat=" + lat
    }
    if (lng != null && lng != "") {
      url += "&lng=" + lng
    }
    if (radius != null && radius != 0) {
      url += "&radius=" + radius
    }

    return this.http.get(url,
      {
        headers
      })
      .pipe(
        map((response) => {
          let res = JSON.parse(JSON.stringify(response));
          if (res.returnValue == '000') {
            return res;
          } else if (res.returnValue == '002') {
            localStorage.removeItem("jwt")
            this.router.navigate(["/signin"])
            throw new Error(res.message);
          } else {
            throw new Error(res.message);
          }
        }),
        catchError((e: Response) => this.handleError(e))
      );
  }

