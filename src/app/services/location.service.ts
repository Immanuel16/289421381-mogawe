  getLocations(uuidProject: string, tag: string, query: string, page: number, offset: number) {

    const headers = new HttpHeaders().set("Content-Type", "application/json").set("Token", localStorage.getItem("jwt"));
    let url = AppConstant.PROJECT_SERVICE_ENDPOINT + AppConstant.API_LOCATION_GET + "?";

    url += "q=" + (query == null ? "" : query)
    if (uuidProject != null) {
      url += "&p=" + uuidProject
    }
    if(tag != null){
      url += "&tag=" + encodeURIComponent(tag)
    }
    url += "&page=" + page
    url += "&offset=" + offset

    return this.http.get(url,
      {
        headers
      })
      .pipe(
        map((response) => {
          let res = JSON.parse(JSON.stringify(response));
          if (res.returnValue == '000') {
            return res;
          } else if (res.returnValue == '002') {
            localStorage.removeItem("jwt")
            this.router.navigate(["/signin"])
            throw new Error(res.message);
          } else {
            throw new Error(res.message);
          }
        }),
        catchError((e: Response) => this.handleError(e))
      );
  }
  
